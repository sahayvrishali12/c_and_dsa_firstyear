#include<stdio.h>
#include<conio.h>
main()
{//opening of main
      int i,j;
      printf("\nTo understand working of nested for loop\n");
      
      for(i=1;i<=5;i++)
      
      {//opening of i
            
            for(j=1;j<=i;j++)
            
            {//opening of j
                             printf("*");
                             
            }//close of j 
                             printf("\n");
                             
      }//close of i 
                             getch();
                             
                             
}//close of main 
                             
